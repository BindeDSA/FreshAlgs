    /// <summary>
    /// The names of the corner positions of the cube. Corner URF e.g. has an U(p), a R(ight) and a F(ront) facelet.
    /// </summary>
    public enum Corner
    {
        Urf = 0,
        Ufl = 1,
        Ulb = 2,
        Ubr = 3,
        Dfr = 4,
        Dlf = 5,
        Dbl = 6,
        Drb = 7,
        Invalid = 8,
    }
}